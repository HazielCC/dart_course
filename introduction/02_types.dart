/* 
Tipos de Datos en Dart
 */

void main() {
  // Enteros
  int numeroEntero = 42;
  print('Número Entero: $numeroEntero');

  // Números de punto flotante
  double numeroDecimal = 3.14;
  print('Número Decimal: $numeroDecimal');

  // Cadenas de texto
  String texto = 'Hola, Dart!';
  print('Texto: $texto');

  // Booleanos
  bool esVerdadero = true;
  print('Booleano: $esVerdadero');

  // Listas
  List<String> listaDeFrutas = ['Manzana', 'Banana', 'Cereza'];
  print('Lista de Frutas: $listaDeFrutas');

  // Mapas
  Map<String, int> edades = {'Alice': 30, 'Bob': 25};
  print('Mapa de Edades: $edades');
}
